export function validateAssessmentAnswers(answers: unknown): answers is number[] {
  if (!Array.isArray(answers) || answers.length !== ASSESSMENT_QUESTIONS.length) {
    return false;
  }
  for (let i = 0; i < answers.length; i++) {
    const a = answers[i];
    const q = ASSESSMENT_QUESTIONS[i]!;
    if (typeof a !== "number" || !Number.isInteger(a) || a < 0 || a >= q.options.length) {
      return false;
    }
  }
  return true;
}

export function scoreAssessment(answers: number[]): {
  correct: number;
  scorePercent: number;
} {
  let correct = 0;
  for (let i = 0; i < ASSESSMENT_QUESTIONS.length; i++) {
    if (answers[i] === ASSESSMENT_QUESTIONS[i]!.correct) correct++;
  }
  return {
    correct,
    scorePercent: Math.round((correct / ASSESSMENT_QUESTIONS.length) * 100),
  };
}
